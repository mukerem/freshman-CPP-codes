#include<iostream>
using namespace std;
int main()
{
cout<<"It is a c++ course ****";
cout<<"for fresh man students"<<endl;//both strings display in a single line
cout<<"It is a c++ course ****"<<endl;
cout<<"for fresh man students 5+10";//both strings display in different  line
cout<<endl<<"It is a c++ course ****\n";
cout<<"for fresh man students "<<5+10;//both strings display in different  line
/*the above comment is line comment used only for one line comment
but this is block comment used for two or more line comments*/
}
